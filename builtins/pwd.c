#include "builtins.h"

int	pwd(char **args)
{
	char	cwd[4096];

	(void)args;
	if (getcwd(cwd, sizeof(cwd)) != NULL)
	{
		write(1, cwd, ft_strlen(cwd));
		write(1, "\n", 1);
		return (0);
	}
	write(2, "pwd: error retrieving current directory\n", 40);
	return (1);
}
